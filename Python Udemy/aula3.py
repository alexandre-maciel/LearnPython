'''
tipos int e float
int -> numero inteiro
o tipo int representa qualquer numero positivo ou negativo
int sem sinal é considerado positivo.
'''

print(11)
print(-11)
print(0)
print(1.2)

'''

float -> numero com ponto flutuante
o tipo float representa qualquer numero positivo ou negativo com ponto flutuante
float sem sinal é considerado positivo

a função type mostra o tipo que o python inferiu ao valor

callable -> chamavel

class

tudo em python é um objeto
'''

print(23.4)
print(-5.2)

# a função, que na verdade é uma classe, retorna o tipo do dados dentro dos parenteses

print(type(5.2))
print(type("Texto"))
print(type(-2))
print(type(True))
