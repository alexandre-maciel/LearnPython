'''
tipo de dado boolean

Ao questionar algo em um programa, só existem duas respostas possiveis: sim (true) e não (false).

Existem vários operadores para questionar, dentre eles o ==, que é um operador lógico que questiona se um valor
é igual ao outro
'''

print (10 == 10)
print (10 == 11)

print(type(10==10))
