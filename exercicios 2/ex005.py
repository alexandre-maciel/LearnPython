'''
5. Contagem Regressiva Simples
Objetivo: Praticar o loop for e a função range().

Instruções:

Use um loop for para imprimir uma contagem regressiva de 5 até 1.

Após o loop, imprima a mensagem: "FOGO!"
'''

print("A bomba vai explodir em... ")

# cria um loop na qual vai fazer a contagem de 5 a 0
for numero in range(5,0,-1):
    print(numero)
print("FOGO!!!")